#!/usr/bin/env node

import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const enPath = path.join(repoRoot, 'docs', 'i18n', 'locales', 'en.json');
const zhCnPath = path.join(repoRoot, 'docs', 'i18n', 'locales', 'zh-CN.json');
const outputPath = path.join(repoRoot, 'UniversalSyncService.Web', 'src', 'i18n', 'generated', 'resources.ts');

function stableSortObject(input) {
  const entries = Object.entries(input).sort(([a], [b]) => a.localeCompare(b));
  return Object.fromEntries(entries);
}

function createTypeFromKeys(keys) {
  return keys.map((key) => `  '${key}': string;`).join('\n');
}

try {
  const [enRaw, zhCnRaw] = await Promise.all([
    readFile(enPath, 'utf8'),
    readFile(zhCnPath, 'utf8'),
  ]);

  const en = stableSortObject(JSON.parse(enRaw));
  const zhCN = stableSortObject(JSON.parse(zhCnRaw));
  const keys = Object.keys(en).sort((a, b) => a.localeCompare(b));

  const outputDir = path.dirname(outputPath);
  await mkdir(outputDir, { recursive: true });

  const generated = `// <auto-generated />\n// 此文件由 scripts/generate-web-i18n.mjs 生成，请勿手动编辑。\n\nexport type LocaleCode = 'en' | 'zh-CN';\n\nexport type I18nKey =\n${keys.map((k) => `  | '${k}'`).join('\n')};\n\nexport type LocaleDictionary = {\n${createTypeFromKeys(keys)}\n};\n\nexport const resources: Record<LocaleCode, LocaleDictionary> = {\n  en: ${JSON.stringify(en, null, 2)},\n  'zh-CN': ${JSON.stringify(zhCN, null, 2)}\n} as const;\n\nexport const fallbackLocale: LocaleCode = 'en';\n`;

  await writeFile(outputPath, generated, 'utf8');
  process.stdout.write(`Generated: ${path.relative(repoRoot, outputPath)}\n`);
} catch (error) {
  const message = error instanceof Error ? error.stack ?? error.message : String(error);
  process.stderr.write(`[generate-web-i18n] Failed: ${message}\n`);
  process.exit(1);
}
