#!/usr/bin/env node

import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const targetDir = path.join(repoRoot, 'docs', 'i18n', 'targets', 'web');
const targetConfigPath = path.join(targetDir, 'target.config.json');

function ensureObject(name, value) {
  if (!value || typeof value !== 'object' || Array.isArray(value)) {
    throw new Error(`${name} 必须是 JSON 对象`);
  }
}

function stableSortObject(input) {
  const entries = Object.entries(input).sort(([a], [b]) => a.localeCompare(b));
  return Object.fromEntries(entries);
}

function createTypeFromKeys(keys) {
  return keys.map((key) => `  '${key}': string;`).join('\n');
}

function parseTargetConfig(rawConfig) {
  ensureObject('web target config', rawConfig);

  const target = typeof rawConfig.target === 'string' ? rawConfig.target.trim() : '';
  if (target !== 'web') {
    throw new Error(`web target 配置无效，target 必须为 web，当前为: ${target || '(empty)'}`);
  }

  const localeDir = typeof rawConfig.localeDir === 'string' && rawConfig.localeDir.trim().length > 0
    ? rawConfig.localeDir.trim()
    : 'locales';

  const locales = Array.isArray(rawConfig.locales)
    ? rawConfig.locales.filter((item) => typeof item === 'string' && item.trim().length > 0).map((item) => item.trim())
    : ['en', 'zh-CN'];

  if (locales.length === 0) {
    throw new Error('web target 配置 locales 不能为空');
  }

  const defaultLocale = typeof rawConfig.defaultLocale === 'string' && rawConfig.defaultLocale.trim().length > 0
    ? rawConfig.defaultLocale.trim()
    : 'en';

  if (!locales.includes(defaultLocale)) {
    throw new Error(`web target 配置 defaultLocale 必须在 locales 中，当前为 ${defaultLocale}`);
  }

  const output = typeof rawConfig.output === 'string' && rawConfig.output.trim().length > 0
    ? rawConfig.output.trim()
    : 'UniversalSyncService.Web/src/i18n/generated/resources.ts';

  return {
    target,
    localeDir,
    locales,
    defaultLocale,
    output,
  };
}

function buildLocaleDictionary(keys, localeData, fallbackData) {
  const resolved = {};
  for (const key of keys) {
    if (Object.prototype.hasOwnProperty.call(localeData, key)) {
      resolved[key] = localeData[key];
      continue;
    }

    if (Object.prototype.hasOwnProperty.call(fallbackData, key)) {
      resolved[key] = fallbackData[key];
      continue;
    }

    resolved[key] = key;
  }

  return stableSortObject(resolved);
}

try {
  const targetConfigRaw = await readFile(targetConfigPath, 'utf8');
  const targetConfig = parseTargetConfig(JSON.parse(targetConfigRaw));

  const localeDataByCode = new Map();
  for (const locale of targetConfig.locales) {
    const localePath = path.join(targetDir, targetConfig.localeDir, `${locale}.json`);
    const localeRaw = await readFile(localePath, 'utf8');
    const parsed = JSON.parse(localeRaw);
    ensureObject(`web locale (${locale})`, parsed);
    localeDataByCode.set(locale, parsed);
  }

  const fallbackData = stableSortObject(localeDataByCode.get(targetConfig.defaultLocale));
  const keys = Object.keys(fallbackData).sort((a, b) => a.localeCompare(b));
  const resourcesObject = {};

  for (const locale of targetConfig.locales) {
    const localeData = localeDataByCode.get(locale) ?? {};
    resourcesObject[locale] = buildLocaleDictionary(keys, localeData, fallbackData);
  }

  const outputPath = path.resolve(repoRoot, targetConfig.output);
  const outputDir = path.dirname(outputPath);
  await mkdir(outputDir, { recursive: true });

  const generated = `// <auto-generated />\n// 此文件由 scripts/generate-web-i18n.mjs 生成，请勿手动编辑。\n// 真源: docs/i18n/targets/web\n\nexport type LocaleCode = ${targetConfig.locales.map((code) => `'${code}'`).join(' | ')};\n\nexport type I18nKey =\n${keys.map((k) => `  | '${k}'`).join('\n')};\n\nexport type LocaleDictionary = {\n${createTypeFromKeys(keys)}\n};\n\nexport const resources: Record<LocaleCode, LocaleDictionary> = ${JSON.stringify(resourcesObject, null, 2)} as const;\n\nexport const fallbackLocale: LocaleCode = '${targetConfig.defaultLocale}';\n`;

  await writeFile(outputPath, generated, 'utf8');
  process.stdout.write(`Generated: ${path.relative(repoRoot, outputPath)} (target=${targetConfig.target})\n`);
} catch (error) {
  const message = error instanceof Error ? error.stack ?? error.message : String(error);
  process.stderr.write(`[generate-web-i18n] Failed: ${message}\n`);
  process.exit(1);
}
